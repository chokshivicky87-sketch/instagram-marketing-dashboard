import os
from PIL import Image, ImageDraw, ImageFilter, ImageOps
from pydantic import BaseModel, Field
from google import genai
from google.genai import types

# ---------------------------------------------------------------------------
# Pydantic Schemas for Structured Gemini Outputs
# ---------------------------------------------------------------------------

class Concept(BaseModel):
    headline: str = Field(description="A catchy, highly engaging headline or hook suited for Instagram marketing.")
    visual_description: str = Field(description="A detailed description of the visual scene to be generated by AI.")
    caption: str = Field(description="Recommended marketing caption for the post, including relevant hashtags.")

class ConceptList(BaseModel):
    concepts: list[Concept] = Field(description="A list of exactly 3 distinct marketing concepts.")

# ---------------------------------------------------------------------------
# Background Sizing Utilities
# ---------------------------------------------------------------------------

def get_background_image(bg_path="assets/background.png") -> tuple[Image.Image, int, int]:
    """
    Loads the background image and returns the Image object along with its dimensions.
    Creates a fallback background if the file does not exist.
    """
    if not os.path.exists(bg_path):
        # Create a default 1080x1080 image if it does not exist
        os.makedirs(os.path.dirname(bg_path), exist_ok=True)
        # Create a simple dark elegant placeholder gradient
        img = Image.new("RGB", (1080, 1080), color=(18, 18, 24))
        draw = ImageDraw.Draw(img)
        draw.rectangle([20, 20, 1060, 1060], outline=(212, 175, 55), width=3) # Gold border
        img.save(bg_path, "PNG")
        
    img = Image.open(bg_path).convert("RGBA")
    width, height = img.size
    return img, width, height

